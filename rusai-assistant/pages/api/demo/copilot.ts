function pickScenario(scene: string, input: string): string {
  const key = scene || detectScene(input)
  const templates = SCENARIOS[key] || SCENARIOS.customer_reply
  return templates[Math.floor(Math.random() * templates.length)]
}

function detectScene(input: string): string {
  const text = input.toLowerCase()
  if (text.includes('报价') || text.includes('цена') || text.includes('стоимость') || text.includes('quotation')) return 'quotation'
  if (text.includes('faq') || text.includes('常见') || text.includes('вопрос') || text.includes('часто')) return 'faq'
  if (text.includes('文案') || text.includes('通知') || text.includes('объявление') || text.includes('письмо')) return 'writing_assist'
  return 'customer_reply'
}

async function callDeepSeek(systemPrompt: string, userMessage: string): Promise<string | null> {
  const key = process.env.DEEPSEEK_API_KEY
  if (!key) return null

  try {
    const controller = new AbortController()
    const timeout = setTimeout(() => controller.abort(), 25000)

    const res = await fetch('https://api.deepseek.com/chat/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${key}`
      },
      body: JSON.stringify({
        model: 'deepseek-chat',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: userMessage }
        ],
        temperature: 0.7,
        max_tokens: 1500
      }),
      signal: controller.signal
    })
    clearTimeout(timeout)

    const data = await res.json()
    if (data.choices?.[0]?.message?.content) {
      return data.choices[0].message.content
    }
    return null
  } catch {
    return null
  }
}

export default async function handler(req: NextApiRequest, res: NextApiResponse) {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' })
  }

  const { input, scene } = req.body || {}
  if (!input || typeof input !== 'string' || input.trim().length === 0) {
    return res.status(400).json({ error: '请输入内容 / Пожалуйста, введите текст' })
  }

  const systemPrompt = `你是专业的中俄双语商务助理（RusAI Copilot）。
用户输入业务问题或场景，你必须用以下格式回复：

=== RU ===
[俄语回复内容]

=== 中文对照 ===
[对应的中文内容]

要求：
- 俄语地道自然、符合母语者习惯
- 中文准确对应
- 用【】标记需要用户自定义的占位内容
- 场景包括：留学咨询、培训课程、商务沟通、报价、FAQ、文案辅助等`

  const aiResult = await callDeepSeek(systemPrompt, input.trim())
  if (aiResult) {
    return res.status(200).json({ result: aiResult })
  }

  const mockResult = pickScenario(scene, input.trim())
  await new Promise(r => setTimeout(r, 300 + Math.random() * 500))

  return res.status(200).json({ result: mockResult })
}
